def t(key: str, lang: str = "fr", **kwargs: Any) -> str:
    """Return translation for key. Use kwargs for str.format placeholders."""
    if key not in TRANSLATIONS:
        return key
    entry = TRANSLATIONS[key]
    if not isinstance(entry, dict) or "fr" not in entry:
        return str(entry)
    text = entry.get(lang) or entry.get("en") or key
    if kwargs:
        text = text.format(**kwargs)
    return text


def get_feature_name(feature_key: str, lang: str = "fr") -> str:
    """Translate a feature/column name to a human-readable medical label."""
    feature_key_lower = feature_key.lower().strip()
    feature_names = TRANSLATIONS.get("feature_names", {})
    if isinstance(feature_names, dict) and feature_key_lower in feature_names:
        row = feature_names[feature_key_lower]
        if isinstance(row, dict):
            return str(row.get(lang) or row.get("en") or feature_key)
    return feature_key


def leaderboard_column_label(col: str, lang: str) -> str:
    """Map PyCaret leaderboard column names to translated headers."""
    c = str(col).strip()
    mapping = {
        "Model": "col_model",
        "Accuracy": "col_accuracy",
        "AUC": "col_auc",
        "Recall": "col_recall",
        "Prec.": "col_precision",
        "Precision": "col_precision",
        "F1": "col_f1",
        "TT (Sec)": "col_time",
        "Time (Sec)": "col_time",
    }
    key = mapping.get(c)
    if key:
        return t(key, lang)
    return c
